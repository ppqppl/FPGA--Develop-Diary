RTC_DateTypeDef Date;
RTC_TimeTypeDef Time;

uint8_t str[] = "ppqppl\r\n";

void HAL_TIM_PeriodElapsedCallback(TIM_HandleTypeDef *htim){
	static int time = 0;
	if(htim->Instance == TIM2){
		time++;
		if(time%100 == 0){
			HAL_GPIO_TogglePin(GPIOA,GPIO_PIN_5);
			HAL_RTC_GetDate(&hrtc,&Date,RTC_FORMAT_BIN);
			HAL_RTC_GetTime(&hrtc,&Time,RTC_FORMAT_BIN);
			printf("%02d年-%02d月-%02d日-",2000+Date.Year,Date.Month,Date.Date);	// Date.WeekDay表示日期
			switch(Date.WeekDay){
				case 1:{
					printf("星期一\r\n");
					break;
				}
				case 2:{
					printf("星期二\r\n");
					break;
				}
				case 3:{
					printf("星期三\r\n");
					break;
				}
				case 4:{
					printf("星期四\r\n");
					break;
				}
				case 5:{
					printf("星期五\r\n");
					break;
				}
				case 6:{
					printf("星期六\r\n");
					break;
				}
				case 7:{
					printf("星期日\r\n");
					break;
				}
				default:{
					printf("没有获取到WeekDay数据!\r\n");
					break;
				}
			}
			printf("%02d时:%02d分:%02d秒\r\n",Time.Hours,Time.Minutes,Time.Seconds);
			HAL_UART_Transmit(&huart1,(uint8_t*)str,sizeof(str),0xFFFF);
			printf("\r\n");
			time = 0;
		}
	}
}

